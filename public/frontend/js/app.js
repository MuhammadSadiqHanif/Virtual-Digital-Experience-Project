function dropDownToggle() {
    element = document.getElementById("drop");
    
    if(element.computedStyleMap().get('opacity').value == 0)
    {
        document.getElementById("drop").style.opacity = "1";
    }
    else
    {
        document.getElementById("drop").style.opacity = "0";
    }
}


